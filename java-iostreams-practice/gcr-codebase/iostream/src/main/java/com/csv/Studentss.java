package com.csv;

public class Studentss {

    public int id;
    public String name;
    public int age;
    public int marks;

    public Studentss() {} 

    public Studentss(int id, String name, int age, int marks) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.marks = marks;
    }
}
