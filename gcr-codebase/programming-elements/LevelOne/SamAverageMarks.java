public class SamAverageMarks{
	public static void main(String[] args){
	//Here are the Obtained marks of Sam in Physics, Maths and Chemistry.
	  double PhysicsMarks = 95;
	  double ChemistryMarks = 96;
	  double MathsMarks = 94;
	  //Calculating the Average marks in PCM.
	  double AverageMarks = ((PhysicsMarks + ChemistryMarks + MathsMarks)/3);
	  System.out.println("Sam's average mark in PCM is "+AverageMarks);
	  
	}
}